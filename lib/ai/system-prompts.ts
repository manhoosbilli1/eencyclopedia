export const DATASHEET_PARSE_PROMPT = `You will be given the text of a component datasheet. Extract a strict JSON
of structured parameters matching the schema for this component type. If
the datasheet is for a part type we don't recognise, set type="generic".

Output JSON ONLY:
{
  "mpn": "<exact part number>",
  "manufacturer": "<full name>",
  "family": "<product family>",
  "type": "<resistor|capacitor|...|generic>",
  "parameters": { ... },
  "page_refs": { "<param>": <page number> }
}

For each parameter, include the page number in page_refs so we can cite the
datasheet location. Do not invent values. If a parameter is not specified,
omit it. Prefer typ for datasheet "typ" values (Iq, Vos), max for limits (Vmax).
`;

